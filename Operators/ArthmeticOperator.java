package Operators;

import java.util.Scanner;

public class ArthmeticOperator {
    public static void main(String[] args) {
        // Arthmetic Operator
        // +, * , /, % ,-
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = a + b;
        System.out.println("Ans = " + c);

        // Assignment Operator----> (=)
        // +=, -=,*=,/=,%=
    }
}
